import csv

# Open the source CSV file for reading
with open("C:\\Users\\nikita\\Desktop\\mark_240805123801.csv", 'r', encoding='utf-8') as csvfile:
    # Open the target CSV file for writing
    with open("clean_name_links.csv", 'w', encoding='utf-8', newline='') as f:
        # Create a CSV writer object
        writer = csv.writer(f)
        # Write the header row
        writer.writerow(['GOODS_NAME', "GOODS_URL"])

        # Read the input CSV file as a dictionary
        data = csv.DictReader(csvfile, delimiter='\t')

        # List of characters or strings to remove
        z = ['character_to_remove_1', 'character_to_remove_2']  # Update with actual characters/strings

        # Loop through each row in the input data
        for row in data:
            # Get the GOODS_NAME field
            st = row['GOODS_NAME']
            # Remove unwanted characters or strings
            for i in z:
                st = st.replace(i, '')
            # Write the cleaned GOODS_NAME and GOODS_URL to the output file
            writer.writerow([st, row["GOODS_URL"]])
